    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        jTextArea1.setText("Prepairing to scan Code.............");
        JFileChooser filechh = new JFileChooser("D:");
        filechh.setAcceptAllFileFilterUsed(false);
        filechh.setFileFilter(extensionFilter);
        filechh.showOpenDialog(null);

        File f = filechh.getSelectedFile();
        URL = f.getAbsolutePath();

        try {

            jTextArea1.setText("");
            new Thread(new Runnable() {

                FileReader fw = new FileReader(URL);
                BufferedReader br = new BufferedReader(fw);
                String Codeline = null;

                @Override
                public void run() {
                    try {
                        while ((Codeline = br.readLine()) != null) {

                            if (Codeline.contains("\t")) {
                                Codeline = Codeline.replaceAll("\t", "    ");
                            }

                            jTextArea1.setText(jTextArea1.getText() + "\n" + Codeline);
                            jTextArea1.setCaretPosition(jTextArea1.getDocument().getLength());
                            Thread.sleep(10);
                        }
                    } catch (Exception e) {
                    }
                }

            }).start();

        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

