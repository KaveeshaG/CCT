        c.setRM_R_GLV_DF(Integer.parseInt(a13.getText()));
        c.setRCM_R_GLV_SF(Integer.parseInt(a15.getText()));
        c.setRCM_R_GLV_DF(Integer.parseInt(a14.getText()));

        model.Create_properties.createFile("Coup.prof", c);
        JOptionPane.showMessageDialog(this, "Successfull");

