        c.setRecursive_cal(Integer.parseInt(a1.getText()));
        c.setRM_call_RM_SF(Integer.parseInt(a2.getText()));
        c.setRM_call_RM_DF(Integer.parseInt(a3.getText()));
        c.setRM_RCM_SF(Integer.parseInt(a4.getText()));
        c.setRM_RCM_DF(Integer.parseInt(a5.getText()));
        c.setRCM_RCM_SF(Integer.parseInt(a11.getText()));
        c.setRCM_RCM_DF(Integer.parseInt(a12.getText()));
