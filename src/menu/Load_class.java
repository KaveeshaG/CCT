    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        jTextArea1.setText("");
        JFileChooser jFileChooser1 = new JFileChooser();
        jFileChooser1.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

        jFileChooser1.showOpenDialog(null);

        File x = jFileChooser1.getSelectedFile();

        try {

            File dir = new File(x.getAbsolutePath());
            for (File file : dir.listFiles()) {

                String path = file.getAbsolutePath();

                jTextArea1.setText(jTextArea1.getText() + "\n" + "//-----------------New Source Code Upload---------------");
                jTextArea1.setText(jTextArea1.getText() + "\n" + "//--------------" + file.getName() + "--------------");
                jTextArea1.setText(jTextArea1.getText() + "\n" + "//`````````````````````````````````````````````````````");

                try {
                    FileReader fw = new FileReader(path);
                    BufferedReader br = new BufferedReader(fw);
                    String line = null;

                    while ((line = br.readLine()) != null) {

                        if (line.contains("\t")) {
                            line = line.replaceAll("\t", "    ");
                        }
                        jTextArea1.setText(jTextArea1.getText() + "\n" + line);

                    }
                    br.close();
                    jTextArea1.setText(jTextArea1.getText() + "\n" + "//```````````````````````````````````````````````````````");
                    jTextArea1.setText(jTextArea1.getText() + "\n" + "//-----------------" + file.getName() + " End---------------");

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }
            JOptionPane.showMessageDialog(this, "Uploaded Success");

        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButton2ActionPerformed
